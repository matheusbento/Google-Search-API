package models;

public class SearchModel {
	String title;
	String url;
	
	public SearchModel() {
		title = "";
		url = "";
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
	

}
